nombre = 'Cesar Quintero'
# INDEXING :
# Posición índice de 'C' es cero:
print(nombre[4])
# SLICING: Colocar una posición más
print(nombre[1:4])
print(nombre[2:7])
print(nombre[7:13])
print(nombre[0:14])
print(nombre[0::2])
# CsrQ
print(nombre[0:13])
print(nombre[0:13:2])
print(nombre[0:13:3])
# Regresivo:
print('Regresivo')
print(nombre[-1::-1])
